	<h1 class="text-2xl font-semibold text-gray-800">Welcome to Who's to Lose!</h1>
	<p>
		Your job is to sell a given story, it may be true, or it may be false! Whether or not people
		believe you is in your hands.
	</p>
	<h2 class="text-xl font-semibold text-gray-800">Game Summary</h2>
	<p class="md:indent-12">
		In this game, players take turns confidently telling embarrassing stories prompted by the host
		regardless of if they are true or not. Other contestants will interrogate their story to
		determine if they are lying. Storytellers win by tricking, interrogators win by detecting. May
		the best embarrassing liar win!
	</p>
	<h2 class="text-xl font-semibold text-gray-800">Story Submission</h2>
	<p class="md:indent-12">
		An important part of the game is pre-submitted stories by the contestants. After submitting this
		agreement, please go to the story submission section of the "Todo" and follow the instructions.
		By submitting stories, you consent to them being used in the show. However, this consent is
		reversible, <strong>as consent should be.</strong>
	</p>
	<h2 class="text-xl font-semibold text-gray-800">Storytelling</h2>
	<p class="md:indent-12">
		As a contestant, you will be prompted to tell a story that may be true or false; the host will
		simply make a claim about you. In any case, you must treat the admission as true, no matter
		what. The other contestants will then ask you questions about your story/experience. If it's a
		true story, good! This should be easy. If it's fake, mix confidence and confusion to make it
		sound like it really happened. Important Rule: Be funny! Commit to the bit. It's more fun, makes
		you look more convincing, and makes the video more fun.
	</p>
	<h2 class="text-xl font-semibold text-gray-800">Interrogating</h2>
	<p class="md:indent-12">
		When other players are telling stories, it's your turn to ask questions. Give room to other
		contestants to ask questions about the stories; don't be an attention whore. Respect people's
		boundaries, but still make things interesting. If you try to metagame, you will be beaten with
		hammers. Important Rule: Be funny! Ask funny questions, and act like a real detective.
	</p>
	<h2 class="text-xl font-semibold text-gray-800">Voting</h2>
	<p class="md:indent-12">
		After the storyteller is finished telling their story, players will honestly and anonymously
		vote if they think the story was real or fake. Again, if you try to metagame: hammers.
	</p>
	<p>Scoring Reminder:</p>
	<ul class="list-inside list-disc">
		<li>If you guess correctly, you get 1 point.</li>
		<li>If you guess wrong, the storyteller gets 1 point.</li>
		<li>Convincing people is just as valuable as detecting lies.</li>
	</ul>
	<h2 class="text-xl font-semibold text-gray-800">Final Reminder</h2>
	<p>This game is to entertain. Lie Confidently. Question Creatively. Lose Gracefully.</p>
	<p>Be respectful to yourself, the other players, and me.</p>
